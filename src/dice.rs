use rand::Rng;

/// 骰子
#[derive(Debug)]
pub struct Dice;

impl Dice {
    #[rustfmt::skip]
    fn dice(num: u8) -> [u8; 8] {
        match num {
            1 => [
                0b00000000,
                0b00011000,
                0b00111100,
                0b01111110,
                0b01111110,
                0b00111100,
                0b00011000,
                0b00000000,
            ],
            2 => [
                0b00000110,
                0b00001111,
                0b00001111,
                0b00000110,
                0b01100000,
                0b11110000,
                0b11110000,
                0b01100000,
            ],
            3 => [
                0b00011000,
                0b00111100,
                0b00111100,
                0b00011000,
                0b11000011,
                0b11100111,
                0b11100111,
                0b11100111,
            ],
            4 =>[
                0b11100111,
                0b11100111,
                0b11100111,
                0b00000000,
                0b00000000,
                0b11100111,
                0b11100111,
                0b11100111,
            ],
            5 => [
                0b11100111,
                0b11100111,
                0b11011011,
                0b00111100,
                0b00111100,
                0b11011011,
                0b11100111,
                0b11100111,
            ],
            6 => [
                0b11100111,
                0b11100111,
                0b00000000,
                0b11100111,
                0b11100111,
                0b00000000,
                0b11100111,
                0b11100111,
            ],
            _ => [0;8],
        }
    }

    pub fn random() -> [u8; 8] {
        let num = rand::thread_rng().gen_range(1..=6);
        Self::dice(num)
    }
}
